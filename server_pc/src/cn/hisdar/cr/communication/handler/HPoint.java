public class HPoint {
    public float x;
    public float y;
    public int toolType;
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	public int getToolType() {
		return toolType;
	}
	public void setToolType(int toolType) {
		this.toolType = toolType;
	}
    
    
}
